package tema1;

public class Main {

        // Metoda pentru definirea a 3 variabile diferite
        public static void myVariables() {
            int numarIntreg = 10;
            String text = "Salut, lume!";
            boolean esteAdevărat = true;

            // Afisam variabilele definite
            System.out.println(numarIntreg);
            System.out.println(text);
            System.out.println(esteAdevărat);
        }

        // Metoda main pentru apelarea metodei myVariables
        public static void main(String[] args) {
            // Apelam metoda myVariables
            myVariables();
        }
    }
